    if not request_queue.empty():
        request_id = request_queue.get()  # –ó–Ω–∏–º–∞—î–º–æ –∑–∞—è–≤–∫—É –∑ —á–µ—Ä–≥–∏
        print(f" –û–±—Ä–æ–±–ª—è—î—Ç—å—Å—è –∑–∞—è–≤–∫–∞ {request_id}...")
