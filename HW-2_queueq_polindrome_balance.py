# –ó–ê–í–î–ê–ù–ù–Ø 3: –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —Å–∏–º–µ—Ç—Ä–∏—á–Ω–æ—Å—Ç—ñ —Å–∏–º–≤–æ–ª—ñ–≤-—Ä–æ–∑–¥—ñ–ª—é–≤–∞—á—ñ–≤ –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é —Å—Ç–µ–∫—É

def is_balanced_brackets(input_string):
    """
    –§—É–Ω–∫—Ü—ñ—è –ø–µ—Ä–µ–≤—ñ—Ä—è—î, —á–∏ —î —Å–∏–º–≤–æ–ª–∏-—Ä–æ–∑–¥—ñ–ª—é–≤–∞—á—ñ —É —Ä—è–¥–∫—É —Å–∏–º–µ—Ç—Ä–∏—á–Ω–∏–º–∏.
    - –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î —Å—Ç–µ–∫ –¥–ª—è –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏ –ø–∞—Ä–Ω–æ—Å—Ç—ñ –¥—É–∂–æ–∫.
    """
    stack = []
    brackets = {')': '(', ']': '[', '}': '{'}

    for char in input_string:
        if char in brackets.values():
            stack.append(char)  # –î–æ–¥–∞—î–º–æ –≤—ñ–¥–∫—Ä–∏–≤–∞—é—á—ñ –¥—É–∂–∫–∏ –¥–æ —Å—Ç–µ–∫—É
        elif char in brackets.keys():
            if stack and stack[-1] == brackets[char]:
                stack.pop()  # –í–∏–¥–∞–ª—è—î–º–æ –ø–∞—Ä—É –∑—ñ —Å—Ç–µ–∫—É
            else:
                return False

    return len(stack) == 0  # –°–∏–º–µ—Ç—Ä–∏—á–Ω–æ, —è–∫—â–æ —Å—Ç–µ–∫ –ø–æ—Ä–æ–∂–Ω—ñ–π

