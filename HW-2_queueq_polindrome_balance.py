# –ó–ê–í–î–ê–ù–ù–Ø 2: –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —á–∏ —î —Ä—è–¥–æ–∫ –ø–∞–ª—ñ–Ω–¥—Ä–æ–º–æ–º, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ –¥–≤–æ—Å—Ç–æ—Ä–æ–Ω–Ω—é —á–µ—Ä–≥—É (deque).

def is_palindrome(input_string):
    """
    –§—É–Ω–∫—Ü—ñ—è –ø–µ—Ä–µ–≤—ñ—Ä—è—î, —á–∏ —î –∑–∞–¥–∞–Ω–∏–π —Ä—è–¥–æ–∫ –ø–∞–ª—ñ–Ω–¥—Ä–æ–º–æ–º.
    - –Ü–≥–Ω–æ—Ä—É—î –ø—Ä–æ–±—ñ–ª–∏ —Ç–∞ —Ä–µ–≥—ñ—Å—Ç—Ä —Å–∏–º–≤–æ–ª—ñ–≤.
    """
    # –ù–æ—Ä–º–∞–ª—ñ–∑—É—î–º–æ —Ä—è–¥–æ–∫: –≤–∏–¥–∞–ª—è—î–º–æ –ø—Ä–æ–±—ñ–ª–∏, –ø–µ—Ä–µ–≤–æ–¥–∏–º–æ –≤ –Ω–∏–∂–Ω—ñ–π —Ä–µ–≥—ñ—Å—Ç—Ä
    normalized_string = ''.join(char.lower() for char in input_string if char.isalnum())

    # –°—Ç–≤–æ—Ä—é—î–º–æ –¥–≤–æ—Å—Ç–æ—Ä–æ–Ω–Ω—é —á–µ—Ä–≥—É (deque) –∑ —Å–∏–º–≤–æ–ª—ñ–≤ —Ä—è–¥–∫–∞
    char_deque = deque(normalized_string)

    # –ü–æ—Ä—ñ–≤–Ω—é—î–º–æ —Å–∏–º–≤–æ–ª–∏ –∑ –æ–±–æ—Ö –∫—ñ–Ω—Ü—ñ–≤ deque
    while len(char_deque) > 1:
        if char_deque.popleft() != char_deque.pop():
            return False

    return True

